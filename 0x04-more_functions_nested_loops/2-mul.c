/**
 * mul-function that multiplies two integers
 * mul:multiplication of a and b
 * product:obtained by multiplying int a and b
 * @a: integer to be taken by a function
 * @b: integer to be taken by a function
 * Return; an integer which is the product of a and b
 */

int mul(int a, int b)
{
	int product = a * b;

	return (product);
}
